#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{	
	long	i;
	int		j;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{	
		if (n < 0)
		{
			write(fd, "-", 1);
			i = n * -1;
		}
		else
			i = n;
		if (i > 9)
		{
			ft_putnbr_fd(i / 10, fd);
		}
		j = i % 10 + '0';
		write(fd, &j, 1);
	}
}
